/**
 * POST /signup
 * Create a new local account.
 */
exports.postSignup = (req, res, next) => {
  req.assert('email', 'Email não é valido').isEmail();
  req.assert('password', 'Senha deve ter pelo menos 4 caracteres').len(4);
  req.assert('confirmPassword', 'Senhas estão diferentes').equals(req.body.password);
  req.sanitize('email').normalizeEmail({ gmail_remove_dots: false });

  const errors = req.validationErrors();

  if (errors) {
    req.flash('errors', errors);
    return res.redirect('/signup');
  }

  const user = new User({
    email: req.body.email,
    password: req.body.password
  });

  User.findOne({ email: req.body.email }, (err, existingUser) => {
    if (err) { return next(err); }
    if (existingUser) {
      req.flash('errors', { msg: 'Uma conta com esse endereço de email já existe' });
      return res.redirect('/signup');
    }
    user.save((err) => {
      if (err) { return next(err); }
      res.redirect('/signupextra?userid='.concat(user._id));
    });
  });
};

